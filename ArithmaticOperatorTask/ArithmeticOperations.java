class ArithmeticOperations{

	public static void main(String[] args){

		byte a = 20;
		short b = 250;

		long  l = a+b;

		System.out.println(l);//270

		int d = 20;
		int e = 6;
		
		int ans = d - e ;
		System.out.println(ans);//14
		int ans1  = d * e;
		System.out.println(ans1);//120

		int ans2 = d /e;
		System.out.println(ans2);//3

		int ans3=d%e;
		System.out.println(ans3);//2
	
	
	}

}