public class DataType {
    public static void main(String[] args) {
        
        // String data type
        System.out.println("Bhakti Wagh");

        //float data type
        System.out.println(500.0);

        //char data type
        System.out.println('A');

        //int data type
        System.out.println(9112606000l);
    
    }
}
