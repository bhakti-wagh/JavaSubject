class TypeCasting2{
	
	public static void main(String[] args){
	
		char c1 = 'a';
		System.out.println(c1);//a
	
		int i1 = c1;
		System.out.println(i1);//97

		long l1 = c1;
		System.out.println(l1);//97

		float f1 = c1;
		System.out.println(f1);//97.0

		double d1 = c1;
		System.out.println(d1);//97.0

	}
}